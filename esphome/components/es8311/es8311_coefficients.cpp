const ES8311Coefficient *ES8311Component::get_coefficient_(uint32_t mclk, uint32_t rate) {
  for (int i = 0; i < (sizeof(coefficients) / sizeof(coefficients[0])); i++) {
    if (coefficients[i].mclk == mclk && coefficients[i].rate == rate)
      return &coefficients[i];
  }
  return nullptr;
}

